import { execSync } from "node:child_process";
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const desktopRoot = resolve(__dirname, "..");
const repoRoot = resolve(desktopRoot, "..");

function runGit(args) {
  try {
    return execSync(`git -C "${repoRoot}" ${args}`, {
      encoding: "utf8",
      stdio: ["ignore", "pipe", "ignore"],
    }).trim();
  } catch {
    return "unknown";
  }
}

const fullHead = runGit("rev-parse HEAD");
const shortHead = runGit("rev-parse --short HEAD");
const branch = runGit("rev-parse --abbrev-ref HEAD");
const buildTime = new Date().toISOString();

const outDir = resolve(desktopRoot, "src", "generated");
mkdirSync(outDir, { recursive: true });

const outFile = resolve(outDir, "buildInfo.ts");
writeFileSync(
  outFile,
  `// Auto-generated by desktop/scripts/write-build-info.mjs. Do not edit by hand.
export const FRONTEND_BUILD_GIT_HEAD = ${JSON.stringify(fullHead)};
export const FRONTEND_BUILD_GIT_SHORT_HEAD = ${JSON.stringify(shortHead)};
export const FRONTEND_BUILD_BRANCH = ${JSON.stringify(branch)};
export const FRONTEND_BUILD_TIME = ${JSON.stringify(buildTime)};
export const FRONTEND_BUILD_REPO_ROOT = ${JSON.stringify(repoRoot)};
`,
  "utf8",
);

console.log(`[build-info] frontend ${shortHead} ${branch} ${buildTime}`);
